package exerc_revisao.atp22.Calculadora;

public class Calculadora {

    public int Soma (int num1, int num2){
        System.out.println("Impressão de dois números inteiros e resultado inteiro");
        int resultado = num1 + num2;
        return resultado;
    }

    public double Soma (double num1, double num2){
        System.out.println("Impressão de dois números double e resultado double");
        double resultado = num1 + num2;
        return resultado;
    }

    public float Soma (float num1, float num2, float num3){
        System.out.println("Impressão de dois números float e resultado float");
        float resultado = num1 + num2;
        return resultado;
    }
    public long Soma (long num1, long num2, long num3){
        System.out.println("Impressão de dois números long e resultado long");
        long resultado = num1 + num2;
        return resultado;
    }
    
}
