package exerc_revisao.atp15_2;

public class Calculadora{
int somar (int n1, int n2){
   int resultado = n1 + n2;
   return resultado;
}
int subtrair (int n1, int n2){
    int resultado = n1 - n2;
    return resultado;
 }
 int multiplicar (int n1, int n2){
    int resultado = n1 * n2;
    return resultado;
 }
 int divisao (int n1, int n2){
    int resultado = n1 / n2;
    return resultado;
 }
}