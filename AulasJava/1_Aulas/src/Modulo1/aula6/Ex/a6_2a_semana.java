package modulo1.aula6.Ex;

public class a6_2a_semana {
    public static void main(String[] args) {
        int num = 1;
        while (num <= 10) ;
        {
            int mult = 0;
            while (num <= 10) {
                System.out.println(num * mult);
                mult++;
            }
            num++;
        }
    }
}